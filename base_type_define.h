#ifndef __BASE_TYPE_DEFINE_H__
#define __BASE_TYPE_DEFINE_H__

#include <linux/types.h>

#ifndef VOID
typedef void		VOID;
#endif

#ifndef CHAR
typedef char		CHAR;
#endif

#ifndef UCHAR
typedef	unsigned char		UCHAR;
#endif

#ifndef USHORT
typedef unsigned short		USHORT;
#endif

#ifndef INT
typedef	int		INT;
#endif

#ifndef UINT
typedef	unsigned int		UINT;
#endif

#ifndef ULONG
typedef	unsigned long		ULONG;
#endif

#ifndef BOOL
typedef	bool		BOOL;
#endif


#endif

